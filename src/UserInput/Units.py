##             Units
## =====================================
m       = 1.0
s        = 1.0
K        = 1.0
kg      = 1.0

cm      = 0.01      * m
km      = 1000.0    * m

mn      = 60        * s
hour    = 60        * mn
day      = 24        * hour
yr        = 365       * day
kyr      = 1e3       * yr
Myr     = 1e6       * yr

Pa       = 1.0
kPa     = 1e3*Pa
MPa    = 1e6*Pa